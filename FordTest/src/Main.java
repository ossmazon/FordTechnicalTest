public class Main {

    public static void covert(int a){

        switch (a){
            case(1):
                System.out.println("I");
                break;
            case(2):
                System.out.println("II");
                break;
            case(3):
                System.out.println("III");
                break;
            case(4):
                System.out.println("IV");
                break;
            case(5):
                System.out.println("V");
                break;
        }
    }

    public static void main(String[] args) {

        covert(5);

    }

}
